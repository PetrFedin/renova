/** Чеклист приёмки по ключевым словам в названии этапа */
const MAP: Record<string, string[]> = {
  демонтаж: ['Мусор вывезен', 'Коммуникации заглушены', 'Соседние зоны защищены'],
  электр: ['Розетки по схеме', 'Заземление проверено', 'Штроба заделана'],
  сантех: ['Герметичность', 'Уклоны соблюдены', 'Доступ к ревизиям'],
  отделк: ['Ровность поверхностей', 'Углы и стыки', 'Чистота после работ'],
  default: ['Качество соответствует договорённости', 'Убран мусор', 'Материалы согласованы', 'Фото зафиксированы'],
};

export function checklistForStage(name: string): string[] {
  const n = name.toLowerCase();
  for (const [k, items] of Object.entries(MAP)) {
    if (k !== 'default' && n.includes(k)) return items;
  }
  return MAP.default;
}
